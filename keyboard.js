const KEY_ESC = 27;
const KEY_W = 87;
const KEY_S = 83;
const KEY_A = 65;
const KEY_D = 68;
const KEY_LEFT = 37;
const KEY_UP = 38;
const KEY_RIGHT = 39;
const KEY_DOWN = 40;
const KEY_SPACE = 32;

document.addEventListener('keydown', function (e) {
  var code = e.keyCode ? e.keyCode : e.which;
  // console.log(code);

  switch (code) {
    case KEY_ESC:
    break;

    case KEY_W:
    break;

    case KEY_S:
    break;

    case KEY_A:
    break;

    case KEY_D:
    break;

    case KEY_LEFT:
    break;

    case KEY_TOP:
    break;

    case KEY_RIGHT:
    break;

    case KEY_DOWN:
    break;
  }
});
